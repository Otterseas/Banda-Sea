// Helper function to get reviews by product
export function getReviewsByProduct(productSlug) {
  const productMap = {
    'surface-tank': ['surface-tank', 'general'],
    'dive-journal': ['dive-journal', 'general'],
    'logbook-booster-pack': ['booster-pack', 'dive-journal'],
    'stickers': ['stickers', 'general'],
    'fun-stickers': ['stickers', 'general'],
    'crochet-creatures': ['general'],
  };
  
  const categories = productMap[productSlug] || ['general'];
  return REVIEWS.filter(r => categories.includes(r.product) && r.message.length > 20);
}

// Get featured reviews for homepage (mix of products, best messages)
export function getFeaturedReviews(count = 6) {
  const featured = REVIEWS
    .filter(r => r.message.length > 50 && r.rating === 5)
    .slice(0, count);
  return featured;
}

// Calculate average rating
export function getAverageRating() {
  const total = REVIEWS.reduce((sum, r) => sum + r.rating, 0);
  return (total / REVIEWS.length).toFixed(1);
}

// Get total review count
export function getTotalReviews() {
  return REVIEWS.length;
}
