// Helper functions
export function getAllStickers() {
  return Object.values(STICKERS).flat();
}

export function getStickerBySlug(slug) {
  return getAllStickers().find(s => s.slug === slug);
}

export function getStickerById(id) {
  return getAllStickers().find(s => s.id === id);
}

export function getStickersByRegion(region) {
  return STICKERS[region] || [];
}

export function getRelatedStickers(sticker, limit = 4) {
  if (!sticker) return [];
  return STICKERS[sticker.region]
    ?.filter(s => s.id !== sticker.id)
    .slice(0, limit) || [];
}

export function getPricePerItem(quantity) {
  if (quantity >= 21) return 1.50;
  if (quantity >= 11) return 1.75;
  return BASE_PRICE;
}

export function getPricingTier(quantity) {
  if (quantity >= 21) return { tier: 'Best Value', color: '#4ade80' };
  if (quantity >= 11) return { tier: 'Great Deal', color: '#D99E30' };
  return { tier: 'Standard', color: '#94a3b8' };
}
